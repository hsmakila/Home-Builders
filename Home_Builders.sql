DROP DATABASE IF EXISTS Home_Builders;
CREATE DATABASE Home_Builders;
USE Home_Builders;

CREATE TABLE Locations (
  location_id INT AUTO_INCREMENT PRIMARY KEY,
  location VARCHAR(100) NOT NULL
);

CREATE TABLE Users (
  user_id INT AUTO_INCREMENT PRIMARY KEY,
  email VARCHAR(100) NOT NULL,
  password VARCHAR(50) NOT NULL,
  type VARCHAR(50) NOT NULL,
  name VARCHAR(100) NOT NULL,
  phone VARCHAR(20),
  description VARCHAR(200),
  location_id INT,
  verification_code VARCHAR(200),
  verified BOOLEAN,
  blocked BOOLEAN DEFAULT 0,
  reset_token VARCHAR(200),
  FOREIGN KEY (location_id) REFERENCES Locations (location_id)
);

CREATE TABLE Category (
  category_id INT AUTO_INCREMENT PRIMARY KEY,
  main_category VARCHAR(100) NOT NULL,
  sub_category VARCHAR(100) NOT NULL
);

CREATE TABLE Services (
  service_id INT AUTO_INCREMENT PRIMARY KEY,
  service_provider_id INT NOT NULL,
  category_id INT NOT NULL,
  title VARCHAR(100) NOT NULL,
  description VARCHAR(200),
  rate DECIMAL(10, 2),
  unit VARCHAR(50),
  is_available BOOLEAN NOT NULL,
  FOREIGN KEY (service_provider_id) REFERENCES Users (user_id),
  FOREIGN KEY (category_Id) REFERENCES Category (category_id)
);

CREATE TABLE job (
  job_id INT AUTO_INCREMENT PRIMARY KEY,
  job_customer_id INT NOT NULL,
  job_service_id INT NOT NULL,
  job_title TEXT,
  job_description VARCHAR(200),
  job_quantity INT,
  job_required_date DATE,
  job_from_date DATE,
  job_to_date DATE,
  job_estimation DECIMAL(10, 2),
  job_advance_paid DECIMAL(10, 2),
  job_status VARCHAR(20), -- (new > accepted > scheduled > ongoing > done | declined | withdraw | canceled)
  job_customer_rating INT,
  job_customer_feedback VARCHAR(200),
  FOREIGN KEY (job_customer_id) REFERENCES Users (user_id),
  FOREIGN KEY (job_service_id) REFERENCES Services (service_id)
);

CREATE TABLE Packages (
  package_id INT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(100) NOT NULL,
  description VARCHAR(200),
  unit_price DECIMAL(10, 2)
);

CREATE TABLE Package_Providers (
  id INT AUTO_INCREMENT PRIMARY KEY,
  package_id INT NOT NULL,
  service_provider_id INT NOT NULL,
  FOREIGN KEY (package_id) REFERENCES Packages (package_id),
  FOREIGN KEY (service_provider_id) REFERENCES Users (user_id)
);

CREATE TABLE News (
  news_id INT AUTO_INCREMENT PRIMARY KEY,
  title VARCHAR(100) NOT NULL,
  description VARCHAR(200),
  added_date DATE
);

CREATE TABLE Chat (
  chat_id INT AUTO_INCREMENT PRIMARY KEY,
  from_id INT NOT NULL,
  to_id INT NOT NULL,
  message TEXT,
  date_time DATETIME,
  seen BOOLEAN,
  FOREIGN KEY (from_id) REFERENCES Users (user_id),
  FOREIGN KEY (to_id) REFERENCES Users (user_id)
);

CREATE TABLE FAQ (
  faq_id INT AUTO_INCREMENT PRIMARY KEY,
  question VARCHAR(200) NOT NULL,
  answer VARCHAR(200),
  date_created DATE
);

CREATE TABLE Notification (
  notification_id INT AUTO_INCREMENT PRIMARY KEY,
  notification_from_user_id INT NOT NULL,
  notification_to_user_id INT NOT NULL,
  notification_title VARCHAR(200) NOT NULL,
  notification_read BOOLEAN DEFAULT 0
);

CREATE TABLE home_design (
  home_design_id INT PRIMARY KEY,
  home_design_service_id INT,
  home_design_no_of_rooms INT,
  home_design_no_of_bathrooms INT,
  home_design_no_of_floors INT,
  home_design_area INT,
  home_design_price INT
);

CREATE TABLE complaints (
    complaint_id INT AUTO_INCREMENT PRIMARY KEY,
    complaint_customer_id INT,
    complaint_service_provider_id INT,
    complaint_text TEXT,
    complaint_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	complaint_status TEXT DEFAULT "new",
    FOREIGN KEY (complaint_customer_id) REFERENCES Users(user_id),
    FOREIGN KEY (complaint_service_provider_id) REFERENCES Users(user_id)
);

